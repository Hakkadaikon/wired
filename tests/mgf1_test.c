#include "test.h"

/* MGF1-SHA256 known-answer vector for seed = 0x00..0x13, mask length 50
 * (spanning two SHA-256 blocks). Computed from RFC 8017 B.2.1 with an
 * independent pure-Python SHA-256 MGF1 reference. */
static const u8 mgf1_seed[20] = {
    0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0a,0x0b,
    0x0c,0x0d,0x0e,0x0f,0x10,0x11,0x12,0x13,
};
static const u8 mgf1_mask[50] = {
    0x4e,0x28,0xba,0xe1,0xaf,0xb3,0x69,0xdc,0x4a,0xcf,0xee,0xa5,
    0xe8,0x56,0x8c,0xdf,0xfd,0x25,0xcc,0xa5,0x97,0x5e,0xed,0xad,
    0x2f,0x36,0x2d,0xda,0x26,0xaf,0xcd,0x0d,0xb6,0x7b,0x9c,0xbd,
    0x11,0x54,0x37,0x86,0x9c,0x4b,0x1f,0xe5,0xf7,0x32,0x9c,0x8a,
    0xb3,0xca,
};

static void test_mgf1_kat(void)
{
    u8 got[50];
    quic_mgf1_sha256(mgf1_seed, 20, got, 50);
    int eq = 1;
    for (usz i = 0; i < 50; i++) if (got[i] != mgf1_mask[i]) eq = 0;
    CHECK(eq == 1);
}

/* A one-byte mask is the first byte of the first block. */
static void test_mgf1_short(void)
{
    u8 got[1];
    quic_mgf1_sha256(mgf1_seed, 20, got, 1);
    CHECK(got[0] == mgf1_mask[0]);
}

void test_mgf1(void)
{
    test_mgf1_kat();
    test_mgf1_short();
}
