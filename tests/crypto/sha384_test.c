#include "crypto/symmetric/hash/hash/sha384.h"

#include "test.h"

/* Compare a digest against its expected bytes. */
static int s384_eq(const u8 got[48], const u8* want) {
  usz diff = 0;
  for (usz i = 0; i < 48; i++) diff |= (usz)(got[i] ^ want[i]);
  return diff == 0;
}

/* FIPS 180-4 / NIST vectors, re-derived with Python hashlib. */
static void test_sha384_vectors(void) {
  static const u8 want_empty[48] = {
      0x38, 0xb0, 0x60, 0xa7, 0x51, 0xac, 0x96, 0x38, 0x4c, 0xd9, 0x32, 0x7e,
      0xb1, 0xb1, 0xe3, 0x6a, 0x21, 0xfd, 0xb7, 0x11, 0x14, 0xbe, 0x07, 0x43,
      0x4c, 0x0c, 0xc7, 0xbf, 0x63, 0xf6, 0xe1, 0xda, 0x27, 0x4e, 0xde, 0xbf,
      0xe7, 0x6f, 0x65, 0xfb, 0xd5, 0x1a, 0xd2, 0xf1, 0x48, 0x98, 0xb9, 0x5b};
  static const u8 want_abc[48] = {
      0xcb, 0x00, 0x75, 0x3f, 0x45, 0xa3, 0x5e, 0x8b, 0xb5, 0xa0, 0x3d, 0x69,
      0x9a, 0xc6, 0x50, 0x07, 0x27, 0x2c, 0x32, 0xab, 0x0e, 0xde, 0xd1, 0x63,
      0x1a, 0x8b, 0x60, 0x5a, 0x43, 0xff, 0x5b, 0xed, 0x80, 0x86, 0x07, 0x2b,
      0xa1, 0xe7, 0xcc, 0x23, 0x58, 0xba, 0xec, 0xa1, 0x34, 0xc8, 0x25, 0xa7};
  static const u8 want_two[48] = {
      0x09, 0x33, 0x0c, 0x33, 0xf7, 0x11, 0x47, 0xe8, 0x3d, 0x19, 0x2f, 0xc7,
      0x82, 0xcd, 0x1b, 0x47, 0x53, 0x11, 0x1b, 0x17, 0x3b, 0x3b, 0x05, 0xd2,
      0x2f, 0xa0, 0x80, 0x86, 0xe3, 0xb0, 0xf7, 0x12, 0xfc, 0xc7, 0xc7, 0x1a,
      0x55, 0x7e, 0x2d, 0xb9, 0x66, 0xc3, 0xe9, 0xfa, 0x91, 0x74, 0x60, 0x39};
  static const u8 two_block[] =
      "abcdefghbcdefghicdefghijdefghijkefghijklfghijklmghijklmnhijklmno"
      "ijklmnopjklmnopqklmnopqrlmnopqrsmnopqrstnopqrstu";
  u8 got[48];
  quic_sha384((const u8*)"", 0, got);
  CHECK(s384_eq(got, want_empty));
  quic_sha384((const u8*)"abc", 3, got);
  CHECK(s384_eq(got, want_abc));
  quic_sha384(two_block, sizeof(two_block) - 1, got);
  CHECK(s384_eq(got, want_two));
}

/* The 128-byte-block padding boundary: 111 bytes fits one block with the
 * 16-byte length field, 112 forces a second block. */
static void test_sha384_pad_boundary(void) {
  static const u8 want_111[48] = {
      0x3c, 0x37, 0x95, 0x50, 0x51, 0xcb, 0x5c, 0x30, 0x26, 0xf9, 0x4d, 0x55,
      0x1d, 0x5b, 0x5e, 0x2a, 0xc3, 0x8d, 0x57, 0x2a, 0xe4, 0xe0, 0x71, 0x72,
      0x08, 0x5f, 0xed, 0x81, 0xf8, 0x46, 0x6b, 0x8f, 0x90, 0xdc, 0x23, 0xa8,
      0xff, 0xcd, 0xea, 0x0b, 0x8d, 0x8e, 0x58, 0xe8, 0xfd, 0xac, 0xc8, 0x0a};
  static const u8 want_112[48] = {
      0x18, 0x7d, 0x4e, 0x07, 0xcb, 0x30, 0x61, 0x03, 0xc6, 0x99, 0x67, 0xbf,
      0x54, 0x4d, 0x0d, 0xfb, 0xe9, 0x04, 0x25, 0x77, 0x59, 0x9c, 0x73, 0xc3,
      0x30, 0xab, 0xc0, 0xcb, 0x64, 0xc6, 0x12, 0x36, 0xd5, 0xed, 0x56, 0x5e,
      0xe1, 0x91, 0x19, 0xd8, 0xc3, 0x17, 0x79, 0xa3, 0x8f, 0x79, 0x1f, 0xcd};
  u8 msg[112], got[48];
  for (usz i = 0; i < sizeof(msg); i++) msg[i] = 'a';
  quic_sha384(msg, 111, got);
  CHECK(s384_eq(got, want_111));
  quic_sha384(msg, 112, got);
  CHECK(s384_eq(got, want_112));
}

void test_sha384(void) {
  test_sha384_vectors();
  test_sha384_pad_boundary();
}
